C INTERVAL: 0.0 - 1.0
      X = ABS(XX)
      IF(X.GE.1.0) GO TO 10
      I = IFIX(20.0*X)+1
      I = INDEXA(I)
      KI3 = X*(X*A(I)+A(I-1))+A(I-2)
      RETURN
C INTERVAL: 1.0 - 3.4
   10 I = IFIX(2.5*(X-1.0))+1
      IF(X.GE.3.4) GO TO 20
      I = INDEXB(I)
      KI3 = X*(X*(X*B(I)+B(I-1))+B(I-2))+B(I-3)
      RETURN
C INTERVAL: 3.4 - 9.0
   20 IF(X.GE.9.0) GO TO 30
      I = INDEXB(I)
      KI3 = X*(X*B(I)+B(I-1))+B(I-2)
      RETURN
C INTERVAL: ABOVE 9.0
   30 KI3 = 0.0
      RETURN
