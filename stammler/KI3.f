      REAL FUNCTION KI3(XX)
C
C 3 AND 4 TERM POWER SERIES CORRESPONDING TO CHEBYSHEV EXPANSIONS.
C
C ACCURACY 1.0E-5, UP TO ARGUMENT 9
C
C INTERVALS OF 0.05 IN RANGE FROM 0.0 TO 0.5 COEFFICIENTS A()
C              0.10 IN RANGE FROM 0.5 TO 1.0 COEFFICIENTS A()
C              0.40 IN RANGE FROM 1.0 TO 5.0 COEFFICIENTS B()
C              0.80 IN RANGE FROM 5.0 TO 9.0 COEFFICIENTS B()
C
      DIMENSION A(45), B(51), INDEXA(20), INDEXB(20)

      DATA    A/ 0.7853961, -0.9990226, 0.7266088,
     1           0.7852024, -0.9912340, 0.6466375,
